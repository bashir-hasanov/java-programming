public class NiceDayOrNot {
    public static void main(String[] args) {
        int temp = 58;
        if (temp >= 70) {
            System.out.println("It is a beautiful day!\nLet`s code Java.");
        } else {
            System.out.println("It is kind of a cold day.\nLet`s stay home and code Java.");
        }
    }
}
